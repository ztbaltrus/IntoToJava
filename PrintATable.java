public class PrintATable {

	public static void main(String[] args) {
		
		// This could have been done with fewer lines with a loop function.
		
		// The print statement uses the Math.pow function to get the power of the exponent 
		
		System.out.println("a" + "\t" + "b" + "\t" + "pow(a, b)");
		System.out.println(1 + "\t" + 2 + "\t" + Math.pow(1, 2));
		System.out.println(2 + "\t" + 3 + "\t" + Math.pow(2, 3));
		System.out.println(3 + "\t" + 4 + "\t" + Math.pow(3, 4));
		System.out.println(4 + "\t" + 5 + "\t" + Math.pow(4, 5));
		System.out.println(5 + "\t" + 6 + "\t" + Math.pow(5, 6));


	}// End of main

}// End of class
